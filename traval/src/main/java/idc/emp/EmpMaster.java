
package idc.emp;

public class EmpMaster {
	
	private int empId;	
	private int mindaEmpId;
	private String empHashKey;
	private String empName;
	private String empEmail;
	private String managerId;
	private String managerName;
	private String managerEmailId;
	private String status;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public int getMindaEmpId() {
		return mindaEmpId;
	}
	public void setMindaEmpId(int mindaEmpId) {
		this.mindaEmpId = mindaEmpId;
	}
	public String getEmpHashKey() {
		return empHashKey;
	}
	public void setEmpHashKey(String empHashKey) {
		this.empHashKey = empHashKey;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpEmail() {
		return empEmail;
	}
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	public String getManagerId() {
		return managerId;
	}
	public void setManagerId(String managerId) {
		this.managerId = managerId;
	}
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public String getManagerEmailId() {
		return managerEmailId;
	}
	public void setManagerEmailId(String managerEmailId) {
		this.managerEmailId = managerEmailId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
 }

